<script>
import colors from "vuetify/lib/util/colors";
export default {
    name: "appcontas-icon",
    props: {
        tipo: {
            type: String,
            default: "colorido",
        },
    },
    computed: {
        color() {
            let corBase = this.cor;

            // if (this.tipo === "colorido") {
            //   const cores = this.cor.split(" ");
            //   if (cores[1] && this.colors[cores[1]]) {
            //     corBase = cores[1];
            //   }
            // }
            if (this.colors[corBase]) {
                return {
                    base: this.colors[corBase].base,
                    dark: this.colors[corBase].darken4,
                    light: this.colors[corBase].lighten3,
                };
            } else if (this.$vuetify.theme.themes.dark[corBase]) {
                const vuetifyCor = this.$vuetify.theme.themes.dark[corBase];
                return {
                    base: vuetifyCor,
                    dark: this.shadeColor(vuetifyCor, -30),
                    light: this.shadeColor(vuetifyCor, 30),
                };
            } else {
                console.log(corBase);
                const vuetifyCor = this.rgbToHex(corBase);
                return {
                    base: vuetifyCor,
                    dark: this.shadeColor(vuetifyCor, -30),
                    light: this.shadeColor(vuetifyCor, 30),
                };
            }
        },
    },
    data() {
        return {
            colors,
            cor: "purple",
        };
    },
    methods: {
        hexToRGB(hex, alpha) {
            var r = parseInt(hex.slice(1, 3), 16),
                g = parseInt(hex.slice(3, 5), 16),
                b = parseInt(hex.slice(5, 7), 16);

            if (alpha) {
                return "rgba(" + r + ", " + g + ", " + b + ", " + alpha + ")";
            } else {
                return "rgb(" + r + ", " + g + ", " + b + ")";
            }
        },
        rgbToHex(a) {
            if (~a.indexOf("#")) return a;
            a = a.replace(/[^\d,]/g, "").split(",");
            return (
                "#" +
                ((1 << 24) + (+a[0] << 16) + (+a[1] << 8) + +a[2])
                    .toString(16)
                    .slice(1)
            );
        },
        shadeColor(color, percent) {
            var R = parseInt(color.substring(1, 3), 16);
            var G = parseInt(color.substring(3, 5), 16);
            var B = parseInt(color.substring(5, 7), 16);

            R = parseInt((R * (100 + percent)) / 100);
            G = parseInt((G * (100 + percent)) / 100);
            B = parseInt((B * (100 + percent)) / 100);

            R = R < 255 ? R : 255;
            G = G < 255 ? G : 255;
            B = B < 255 ? B : 255;

            var RR =
                R.toString(16).length == 1
                    ? "0" + R.toString(16)
                    : R.toString(16);
            var GG =
                G.toString(16).length == 1
                    ? "0" + G.toString(16)
                    : G.toString(16);
            var BB =
                B.toString(16).length == 1
                    ? "0" + B.toString(16)
                    : B.toString(16);

            return "#" + RR + GG + BB;
        },
    },
    mounted() {
        console.log(this);
        const color = this.$el.style.color;
        if (color && color !== "") {
            console.log("color", color);
            this.cor = color;
            return;
        }
        const classes = [...this.$el.classList];
        const index = classes.findIndex((item) => {
            return /--text/.exec(item);
        });
        let cor;
        if (index !== -1) {
            cor = classes[index];
            const textIndex = cor.indexOf("--text");
            cor = cor.substring(0, textIndex);

            const corArray = cor.split("-");
            if (corArray.length > 1) {
                const dualColor =
                    corArray[0] +
                    corArray[1].charAt(0).toUpperCase() +
                    corArray[1].slice(1);
                cor = dualColor;
            }
        } else {
            cor = "purple";
        }
        this.cor = cor;
        console.log(cor);
    },
};
